<b> How are the yurts furnished?</b>
<p> Each yurt is furnished with a queen-size bed with down quilt and gas- fired<br>
    stove. The luxury camping experience also includes electricity and a sink with <br>
    hor an cold running water. Shower and restroom facilities are located in the lodge. </p>

<b> What should I bring? </b>
